"""rasen 螺旋 — kotoba pywasm component entry (ADR-2606101000).

Implements the three `etzhayyim:rasen/rasen-actor` world exports (wit/world.wit) as
module-level functions, the shape componentize-py binds. The bounded PUBLIC seed is embedded
as a read-only string (`wasm/_embedded_seed.py`, generated by `build.sh`) so the component
needs NO filesystem or network at runtime — G1 holds in WASM (no individual genotypes; the
component cannot leak what it does not contain).

Runnable as plain Python too (dev mode): if the embedded seed module is absent it falls back
to reading `data/seed-genome-graph.kotoba.edn`, so `python3 wasm/app.py {analyze|datoms|
coverage}` verifies the exact export bodies before the componentize-py build.
"""
from __future__ import annotations
import sys
import json
import pathlib

_HERE = pathlib.Path(__file__).resolve().parent
sys.path.insert(0, str(_HERE.parent / "methods"))

import analyze as _analyze          # noqa: E402  (pure-stdlib methods)
import datom_emit as _datom_emit    # noqa: E402
import coverage_report as _coverage  # noqa: E402


def _seed_text() -> str:
    """Embedded seed (WASM) or on-disk seed (dev). Bounded PUBLIC reference only."""
    try:
        from _embedded_seed import SEED_EDN  # generated by build.sh; absent in dev
        return SEED_EDN
    except ImportError:
        return (_HERE.parent / "data" / "seed-genome-graph.kotoba.edn").read_text(encoding="utf-8")


def _load():
    forms = _analyze.read_edn(_seed_text())
    nodes, edges = {}, []
    for f in forms:
        if not isinstance(f, dict):
            continue
        if ":genome/id" in f:
            nodes[f[":genome/id"]] = f
        elif ":en/from" in f and ":en/to" in f:
            edges.append(f)
    return nodes, edges


# ── world exports ─────────────────────────────────────────────────────────────
def analyze() -> str:
    nodes, edges = _load()
    res = _analyze.analyze(nodes, edges)

    def rows(d, n=20):
        return [{"id": nid, "label": nodes.get(nid, {}).get(":genome/label", nid),
                 "score": round(v, 4)} for nid, v in
                sorted(d.items(), key=lambda kv: (-kv[1], kv[0]))[:n]]

    return json.dumps({"care": rows(res["care"]), "burden": rows(res["burden"]),
                       "pleiotropy": rows(res["pleiotropy"])}, ensure_ascii=False)


def datoms(tx: int = 1) -> str:
    nodes, edges = _load()
    res = _analyze.analyze(nodes, edges)
    return _datom_emit.emit(nodes, edges, res, int(tx))


def coverage() -> str:
    nodes, edges = _load()
    return _coverage.report(nodes, edges)


if __name__ == "__main__":
    cmd = sys.argv[1] if len(sys.argv) > 1 else "analyze"
    if cmd == "analyze":
        print(analyze())
    elif cmd == "datoms":
        print(datoms(int(sys.argv[2]) if len(sys.argv) > 2 else 1))
    elif cmd == "coverage":
        print(coverage())
    else:
        print(f"unknown export: {cmd} (analyze|datoms|coverage)", file=sys.stderr)
        sys.exit(1)
